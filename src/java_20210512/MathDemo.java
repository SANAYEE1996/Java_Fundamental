package java_20210512;

public class MathDemo {

	public static void main(String[] args) {
		
		System.out.println(Math.ceil(100.003));
		System.out.println(Math.floor(100.003));
		System.out.println(Math.round(100.2837f));
		System.out.println(Math.round(1.2837817912E02));
		System.out.println(Math.abs(-45.67));
		System.out.println(Math.pow(2, 20));
		System.out.println(Math.max(-381, -391));
		System.out.println(Math.min(392, -19));
		System.out.println(Math.sqrt(1000));
		System.out.println((int)(Math.random()*1000+1));
		
	}

}
